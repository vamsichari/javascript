        let x="krishna vamsi";

        //length
        console.log(x.length);

        //replace
        a=x.replace('a','s');
        console.log(a);

        //replaceAll
        b=x.replaceAll('m','q');
        console.log(b);

        //indexOf
        c=x.indexOf('H');
        console.log(c);

        //lastIndexOf
        d=x.lastIndexOf('s');
        console.log(d);

        //startWith
        e=x.startsWith('s');
        console.log(e);

        //endWith
        f=x.endsWith('i');
        console.log(f);

        //toUpperCase
        g=x.toUpperCase();
        console.log(g);

        //tolowerCase
        h=x.toLowerCase();
        console.log(h);

        //includes
        i=x.includes('m');
        console.log(i);

        //repeat
        j=x.repeat('4');
        console.log(j);
        
        //charAt
        k=x.charAt('10');
        console.log(k);

        //charCodeAt
        l=x.charCodeAt(x.length-8);
        console.log(l);

        //subString
        m=x.substring(2,x.length-2);
        console.log(m);